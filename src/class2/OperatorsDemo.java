package class2;

public class OperatorsDemo {
    public static void main(String[] args) {

        int number1=10;
        int number2=20;
        System.out.println(number1+number2); // addition
        // Put your cursor anywhere on a line and press Ctrl+D or CMD+D to duplicate it
        System.out.println(number1-number2); // subtraction
        System.out.println(number1*number2); // multiplying
        System.out.println(number1/number2); // division

        /*
        if you have to store whole numbers always use int
        if you have to store decimal numbers always use double
        if you have to store character you can always use String
         */


    }
}
