package class7;

public class ForLoops3 {
    public static void main(String[] args) {

        /*
        When you know exactly how many times you want to loop through a block of code, use the for loop instead of a while loop.
        when we don't know how many times we need to repeat a block of code we should be going with while loop most of the time
        it happens when the user input is involved
         */

        for (int i=0; i<10; i++) {
            System.out.println(i);
        }

        /*
        int i=0;
        i < 10
          System.out.println(i);
          i++
          i < 10;
            System.out.println(i);
         */
    }
}
