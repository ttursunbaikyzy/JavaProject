package classes.class9;

public class ArraysDemo3 {
    public static void main(String[] args) {

        // array: A B c d e F

        char [] letters={'A', 'B', 'c', 'd', 'e', 'F'};

        for (int i = 0; i < letters.length; i++) {
            System.out.print(letters[i]+" ");
        }

    }
}
