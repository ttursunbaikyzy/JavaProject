package videos.videos1;

public class Video2 {
    public static void main(String[] args) {

        // short hand operators +=, -=, *= ....

        int i=5;
        i += 2;  // i=i+2;

        System.out.println(i);

        byte b = 8;
        // b= b * 2.5; // double 20.0    ---- b *= 2.5
        b *= 2.5; // Type(E1) op E2 // b * 2.5
        System.out.println(b);

    }
}
