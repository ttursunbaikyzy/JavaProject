package homework.homework5;

public class Task6ReDo {
    public static void main(String[] args) {
        /* Print the following pattern:
         *

         *

         *
         **
         *
         **
         *

         */


        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 1; j++) {
                System.out.println("*");
            }
            System.out.println();
        }

        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 1; j++) {
                System.out.println("*");
            }
            System.out.println("**");
        }
        System.out.println("*");







    }
}
